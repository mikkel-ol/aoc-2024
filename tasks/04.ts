const arr = input.split("\n");

type Direction = "N" | "NE" | "E" | "SE" | "S" | "SW" | "W" | "NW";
type Coordinate = [number, number];

type StarMask = {
  [key in Direction]: [Coordinate, Coordinate, Coordinate, Coordinate];
};

type XMask = [
  [Coordinate, Coordinate, Coordinate],
  [Coordinate, Coordinate, Coordinate]
];

function coordinates(x: number, y: number): StarMask {
  return {
    N: [
      [x, y],
      [x, y - 1],
      [x, y - 2],
      [x, y - 3],
    ],
    NE: [
      [x, y],
      [x + 1, y - 1],
      [x + 2, y - 2],
      [x + 3, y - 3],
    ],
    E: [
      [x, y],
      [x + 1, y],
      [x + 2, y],
      [x + 3, y],
    ],
    SE: [
      [x, y],
      [x + 1, y + 1],
      [x + 2, y + 2],
      [x + 3, y + 3],
    ],
    S: [
      [x, y],
      [x, y + 1],
      [x, y + 2],
      [x, y + 3],
    ],
    SW: [
      [x, y],
      [x - 1, y + 1],
      [x - 2, y + 2],
      [x - 3, y + 3],
    ],
    W: [
      [x, y],
      [x - 1, y],
      [x - 2, y],
      [x - 3, y],
    ],
    NW: [
      [x, y],
      [x - 1, y - 1],
      [x - 2, y - 2],
      [x - 3, y - 3],
    ],
  };
}

function xMas(x: number, y: number): XMask {
  return [
    [
      [x - 1, y - 1],
      [x, y],
      [x + 1, y + 1],
    ],
    [
      [x + 1, y - 1],
      [x, y],
      [x - 1, y + 1],
    ],
  ];
}

function part1() {
  let matches = 0;

  for (let i = 0; i < arr.length; i += 1) {
    for (let j = 0; j < arr[i].length; j += 1) {
      const coor = coordinates(i, j);

      const strings = Object.values(coor)
        .map((c) => c.map(([x, y]) => arr[y]?.[x]))
        .map((x) => x.join(""));

      matches += strings.reduce((acc, x) => {
        if (x === "XMAS") return acc + 1;
        return acc;
      }, 0);
    }
  }

  return matches;
}

function part2() {
  let matches = 0;

  for (let i = 0; i < arr.length; i += 1) {
    for (let j = 0; j < arr[i].length; j += 1) {
      const coor = xMas(i, j);

      const strings = coor
        .map((c) => c.map(([x, y]) => arr[y]?.[x]))
        .map((x) => x.join(""));

      if (strings.every((x) => x === "SAM" || x === "MAS")) matches += 1;
    }
  }

  return matches;
}

export default [part1(), part2()];
